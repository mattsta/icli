## Seconday helper where we also add mappings for FUTS_EXCHANGE[tradingClass] => mapping.


def addTradingClassToTopLevel():
    fuadd = {}
    for name, sym in FUTS_EXCHANGE.items():
        # Populating trading class as a top-level lookup symbol if it doesn't match the IBKR symbol name
        # e.g. /SDA is /SPXDIVAN on IBKR and it doesn't work right etc.
        if sym.tradingClass != name:
            fuadd[sym.tradingClass] = sym

    fk = fuadd.keys()
    fek = FUTS_EXCHANGE.keys()

    both = fk & fek
    assert (
        not both
    ), f"Trading Class and Futures Symbols have conflicting names: {', '.join(both)}"

    return fuadd


FUTS_EXCHANGE |= addTradingClassToTopLevel()

# We have ANOTHER dataset for _different_ futures details.
# The above FUTS_EXCHANGE is the official IBKR symbol to exchange mapping, but we also have some extra
# data we may want to use for showing users various details too.


@dataclass
class FutureDetail:
    symbol: str
    exchange: str
    name: str

    # what this future represents
    size: str

    # exchange months in exchange format of:
    #  Jan Feb Mar Apr May Jun Jul Aug Sep Oct Nov Dec
    #    F   G   H   J   K   M   N   Q   U   V   X   Z
    months: str

    # Decimal tick per instrument (same for all series)
    tick: Decimal

    # number of decimal points for displaying quotes and placing orders (minimum: 0, maximum 8)
    decimals: int

    # string representing currency value per 'tick' change.
    valuePerTick: str


def buildTickDetail():
    import pandas as pd

    # uh, this provider changed their URL or went out of business or something. This no longer works.
    # We could find another data service and adapt their table formats to this extractor eventually.
    df = pd.read_html("https://futuresonline.com/education/products/specifications/")
    syms = {}

    # first row is their futures symbol to month mapping table, so skip it
    for table in df[1:]:
        for idx, row in table.iterrows():
            digits = Decimal(row.Tick.split()[0])
            valuePerTick = row.Tick.split()[-1]
            syms[row.Symbol] = FutureDetail(
                symbol=row.Symbol,
                name=row.Name,
                tick=digits,
                decimals=-digits.as_tuple().exponent,  # type: ignore
                valuePerTick=valuePerTick,
                months=row.Months,
                exchange=row.Exchange,
                size=row.Size,
            )

    return syms


